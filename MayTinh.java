
public class MayTinh
{
    public int sum(int a, int b){
        int result = a + b;
        return result;
    }
    
    public int substract(int a, int b){
        int result = a - b;
        return result;
    }
    
    public int multiple(int a, int b){
        int result = a * b;
        return result;
    }
    
    public float divide(float a, float b){
        float result = a / b;
        return result;
    }
}
